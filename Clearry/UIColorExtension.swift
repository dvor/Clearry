import UIKit

extension UIColor {
    class func mainColor() -> UIColor {
        return UIColor.systemYellow
    }

    class func secondaryColor() -> UIColor {
        return UIColor.systemGray
    }

    class func recycleBinColor() -> UIColor {
        return UIColor.systemGray4
    }
}
